void ATankPlayerController::BeginPlay()
{
	Super::BeginPlay();
	auto ControlledTank = GetControlledTank();
	if (!ControlledTank)
	{
		UE_LOG(LogTemp, Error, TEXT("Controlled tank not found"));
	}
	else
	{
		UE_LOG(LogTemp, Warning, TEXT("Player controller is %s"), *(ControlledTank->GetName()));
	}
}

